                for (int i = 0; i < staff_list_skillset.getModel().getSize(); i++) {
                    skillset = staff_list_skillset.getModel().getElementAt(i) + "," + skillset;
                }
                statement.setString(8, skillset);
                statement.setString(9, staff_tf_address.getText());

